package com.loan.models;

import java.util.Date;

public class PropertyDetails {
int propertyId;
String acresOfLand;
double landValueInDollar;
String produceOnLand;
String appraisedBy;
Date valuationDate;
String fieldRepresentative;
String propertyCountry;
String propertyState;
public int getPropertyId() {
	return propertyId;
}
public void setPropertyId(int propertyId) {
	this.propertyId = propertyId;
}
public String getAcresOfLand() {
	return acresOfLand;
}
public void setAcresOfLand(String acresOfLand) {
	this.acresOfLand = acresOfLand;
}
public double getLandValueInDollar() {
	return landValueInDollar;
}
public void setLandValueInDollar(double landValueInDollar) {
	this.landValueInDollar = landValueInDollar;
}
public String getProduceOnLand() {
	return produceOnLand;
}
public void setProduceOnLand(String produceOnLand) {
	this.produceOnLand = produceOnLand;
}
public String getAppraisedBy() {
	return appraisedBy;
}
public void setAppraisedBy(String appraisedBy) {
	this.appraisedBy = appraisedBy;
}
public Date getValuationDate() {
	return valuationDate;
}
public void setValuationDate(Date valuationDate) {
	this.valuationDate = valuationDate;
}
public String getFieldRepresentative() {
	return fieldRepresentative;
}
public void setFieldRepresentative(String fieldRepresentative) {
	this.fieldRepresentative = fieldRepresentative;
}
public String getPropertyCountry() {
	return propertyCountry;
}
public void setPropertyCountry(String propertyCountry) {
	this.propertyCountry = propertyCountry;
}
public String getPropertyState() {
	return propertyState;
}
public void setPropertyState(String propertyState) {
	this.propertyState = propertyState;
}


}
